package scripts;

class InputInformation {
    final String path;
    final int x;
    final int y;
    final int result;
    final int from;
    final int to;
    InputInformation(String path, int x, int y, int result, int from, int to) {
        this.path = path;
        this.x = x;
        this.y = y;
        this.result = result;
        this.from = from;
        this.to = to;
    }
}
